#include "libft.h"

int		ft_putcstr(const char *s, char c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			ft_putchar(s[i]);
		else
			break ;
		++i;
	}
	return (i);
}
