#include "libft.h"

char		*ft_strcsub(char const *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			++i;
		else
			break ;
	}
	return (ft_strsub(s, 0, i));
}
