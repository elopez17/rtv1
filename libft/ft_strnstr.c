#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t n)
{
	size_t	len2;

	if (*little == '\0')
		return ((char *)big);
	len2 = ft_strlen(little);
	while (*big != '\0' && n-- >= len2)
	{
		if (*big == *little && ft_memcmp(big, little, len2) == 0)
			return ((char *)big);
		big++;
	}
	return (NULL);
}
