#include <libft.h>

int	ft_numlen(int nbr)
{
	int len;

	if (nbr < 0)
	{
		len = 2;
		nbr = -nbr;
	}
	else
		len = 1;
	while (nbr /= 10)
		++len;
	return (len);
}
