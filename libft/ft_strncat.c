#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char *pres1;

	pres1 = s1;
	while (*pres1 != '\0')
	{
		pres1++;
	}
	while (len-- > 0 && *s2 != '\0')
	{
		*pres1++ = *s2++;
	}
	*pres1 = '\0';
	return (s1);
}
