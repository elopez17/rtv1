#include <libft.h>

void	free_2d(char ***arr_2d)
{
	int i;

	i = -1;
	while ((*arr_2d)[++i])
		ft_strdel(&(*arr_2d)[i]);
	free(*arr_2d);
	*arr_2d = 0;
}
